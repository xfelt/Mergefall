#!/usr/bin/env python3
"""
Procedural audio generator for Merge Survivor: Board Quest.

Synthesizes all gameplay SFX and two music loops from scratch using only the
Python standard library (no numpy, no external samples). Output is 16-bit mono
PCM WAV written to Assets/_Project/Resources/Audio/.

License: CC0 / public domain. These files are 100% generated by this script;
there is no third-party sample content. Re-run to regenerate identically
(the synth is deterministic; any noise is seeded).

Usage:
    python Tools/generate_audio.py
"""

import math
import os
import random
import struct
import wave

SR = 44100  # sample rate
OUT_DIR = os.path.join(
    os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
    "Assets", "_Project", "Resources", "Audio",
)

# ---------------------------------------------------------------------------
# Low-level helpers (operate on python lists of float samples in [-1, 1])
# ---------------------------------------------------------------------------

NOTE_BASE = {"C": 0, "D": 2, "E": 4, "F": 5, "G": 7, "A": 9, "B": 11}


def note_freq(name):
    """'A4' -> 440.0, 'C5' -> ~523.25. Sharps written as 'C#5'."""
    letter = name[0].upper()
    idx = 1
    semis = NOTE_BASE[letter]
    if len(name) > idx and name[idx] == "#":
        semis += 1
        idx += 1
    octave = int(name[idx:])
    midi = (octave + 1) * 12 + semis
    return 440.0 * (2.0 ** ((midi - 69) / 12.0))


def adsr(n, a, d, s_level, r):
    """Build an n-sample ADSR amplitude envelope (times in samples)."""
    env = [0.0] * n
    for i in range(n):
        if i < a:
            env[i] = i / max(1, a)
        elif i < a + d:
            t = (i - a) / max(1, d)
            env[i] = 1.0 + (s_level - 1.0) * t
        elif i < n - r:
            env[i] = s_level
        else:
            t = (i - (n - r)) / max(1, r)
            env[i] = s_level * (1.0 - t)
    return env


def osc(freq, n, kind="sine", phase=0.0, harmonics=None):
    """Generate n samples of an oscillator. harmonics: list of (mult, amp)."""
    out = [0.0] * n
    w = 2.0 * math.pi * freq / SR
    if harmonics is None:
        if kind == "sine":
            harmonics = [(1, 1.0)]
        elif kind == "triangle":
            harmonics = [(1, 1.0), (3, -1 / 9.0), (5, 1 / 25.0), (7, -1 / 49.0)]
        elif kind == "saw":
            harmonics = [(k, ((-1) ** (k + 1)) / k) for k in range(1, 10)]
        elif kind == "square":
            harmonics = [(k, 1.0 / k) for k in (1, 3, 5, 7, 9)]
        elif kind == "bell":
            harmonics = [(1, 1.0), (2.01, 0.5), (2.99, 0.32), (4.2, 0.18), (5.4, 0.1)]
        else:
            harmonics = [(1, 1.0)]
    for mult, amp in harmonics:
        wm = w * mult
        for i in range(n):
            out[i] += amp * math.sin(wm * i + phase)
    return out


def tone(freq, dur, kind="sine", a=0.005, d=0.03, s=0.7, r=0.05, gain=0.5):
    n = int(dur * SR)
    sig = osc(freq, n, kind)
    env = adsr(n, int(a * SR), int(d * SR), s, int(r * SR))
    return [sig[i] * env[i] * gain for i in range(n)]


def noise(dur, gain=0.5, seed=0):
    rng = random.Random(seed)
    n = int(dur * SR)
    return [(rng.random() * 2 - 1) * gain for _ in range(n)]


def lowpass(sig, cutoff):
    """One-pole low-pass filter."""
    if not sig:
        return sig
    rc = 1.0 / (2 * math.pi * cutoff)
    dt = 1.0 / SR
    alpha = dt / (rc + dt)
    out = [0.0] * len(sig)
    out[0] = sig[0] * alpha
    for i in range(1, len(sig)):
        out[i] = out[i - 1] + alpha * (sig[i] - out[i - 1])
    return out


def mix(target, source, at=0):
    """Add source into target at sample offset `at`, growing target as needed."""
    end = at + len(source)
    if end > len(target):
        target.extend([0.0] * (end - len(target)))
    for i in range(len(source)):
        target[at + i] += source[i]
    return target


def reverb(sig, delays_ms=(37, 53, 71), decay=0.28):
    """Cheap multi-tap feedback-free reverb for a little space."""
    out = list(sig)
    for dm in delays_ms:
        d = int(dm * SR / 1000)
        for i in range(d, len(sig)):
            out[i] += sig[i - d] * decay
        decay *= 0.7
    return out


def normalize(sig, peak=0.89):
    m = max((abs(x) for x in sig), default=0.0)
    if m < 1e-6:
        return sig
    g = peak / m
    return [x * g for x in sig]


def soft_clip(sig):
    return [math.tanh(x) for x in sig]


def write_wav(path, sig, normalize_peak=0.89):
    sig = normalize(sig, normalize_peak)
    frames = bytearray()
    for x in sig:
        v = int(max(-1.0, min(1.0, x)) * 32767)
        frames += struct.pack("<h", v)
    with wave.open(path, "wb") as w:
        w.setnchannels(1)
        w.setsampwidth(2)
        w.setframerate(SR)
        w.writeframes(bytes(frames))
    print(f"  wrote {os.path.relpath(path, OUT_DIR)}  ({len(sig)/SR:.2f}s)")


