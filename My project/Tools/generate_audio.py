def sfx_pickup():
    # Quick soft "tick/blip" with a tiny upward pitch chirp.
    n = int(0.09 * SR)
    out = [0.0] * n
    for i in range(n):
        t = i / n
        f = 520 + 340 * t
        out[i] = math.sin(2 * math.pi * f * i / SR)
    env = adsr(n, int(0.002 * SR), int(0.02 * SR), 0.4, int(0.05 * SR))
    out = [out[i] * env[i] for i in range(n)]
    return out


def sfx_merge():
    # Bright rising chime: 3-note major arpeggio with bell timbre + sparkle.
    out = []
    notes = ["C5", "E5", "G5"]
    step = int(0.075 * SR)
    for k, nm in enumerate(notes):
        t = tone(note_freq(nm), 0.45, kind="bell", a=0.002, d=0.12, s=0.25, r=0.3, gain=0.5)
        mix(out, t, k * step)
    # high sparkle tail
    mix(out, tone(note_freq("C6"), 0.5, kind="bell", a=0.002, d=0.2, s=0.15, r=0.3, gain=0.28),
        len(notes) * step)
    return reverb(out, decay=0.22)


def sfx_fight_start():
    # Drum thump + rising battle horn.
    out = []
    # kick: pitch-dropping sine + click
    n = int(0.22 * SR)
    kick = [0.0] * n
    for i in range(n):
        t = i / n
        f = 150 * (1 - t) + 45
        kick[i] = math.sin(2 * math.pi * f * i / SR) * (1 - t) ** 1.5
    mix(out, kick, 0)
    mix(out, lowpass(noise(0.04, 0.5, seed=1), 1800), 0)  # transient click
    # horn: detuned saws swelling up a fifth
    horn = []
    base = note_freq("G3")
    for det in (0.0, 4.0, -3.0):
        horn_v = tone(base + det, 0.55, kind="saw", a=0.06, d=0.1, s=0.8, r=0.18, gain=0.16)
        mix(horn, horn_v, 0)
    horn = lowpass(horn, 2400)
    mix(out, horn, int(0.06 * SR))
    return soft_clip(out)


def sfx_win():
    # Triumphant ascending fanfare: C E G C (major) bright + reverb tail.
    out = []
    seq = [("C5", 0.0), ("E5", 0.10), ("G5", 0.20), ("C6", 0.32)]
    for nm, t in seq:
        v = tone(note_freq(nm), 0.7, kind="bell", a=0.003, d=0.15, s=0.45, r=0.45, gain=0.42)
        # add octave shimmer
        v2 = tone(note_freq(nm) * 2, 0.6, kind="sine", a=0.003, d=0.2, s=0.2, r=0.35, gain=0.12)
        mix(v, v2, 0)
        mix(out, v, int(t * SR))
    # final sustained chord
    chord_at = int(0.32 * SR)
    for nm in ("C5", "E5", "G5", "C6"):
        mix(out, tone(note_freq(nm), 0.9, kind="triangle", a=0.01, d=0.25, s=0.4, r=0.5, gain=0.16),
            chord_at)
    return reverb(out, decay=0.3)


def sfx_loss():
    # Descending minor "wah-wahn" sting.
    out = []
    seq = [("E4", 0.0), ("D4", 0.16), ("C4", 0.32), ("A3", 0.5)]
    for nm, t in seq:
        v = tone(note_freq(nm), 0.6, kind="saw", a=0.01, d=0.1, s=0.6, r=0.3, gain=0.22)
        v = lowpass(v, 1500)
        mix(out, v, int(t * SR))
    # low pitch-bend "drop" at the end
    n = int(0.5 * SR)
    drop = [0.0] * n
    for i in range(n):
        t = i / n
        f = note_freq("A3") * (1 - 0.35 * t)
        drop[i] = math.sin(2 * math.pi * f * i / SR) * (1 - t)
    mix(out, [x * 0.3 for x in drop], int(0.5 * SR))
    return soft_clip(reverb(out, decay=0.2))


