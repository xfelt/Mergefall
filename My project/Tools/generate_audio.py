# Music loops
# ---------------------------------------------------------------------------

def build_loop(bpm, bars, chords, lead, kind_pad="triangle", kind_lead="sine",
               kick=True, lead_gain=0.16, pad_gain=0.09, seed=7):
    """Generic loop builder. chords: list of chord (list of note names) per bar.
    lead: list of (note, start_beat, dur_beats) over the whole loop (beat units)."""
    beat = 60.0 / bpm
    bar_len = int(beat * 4 * SR)
    total = bar_len * bars
    out = [0.0] * total

    # Pads: sustain each bar's chord softly.
    for b in range(bars):
        chord = chords[b % len(chords)]
        at = b * bar_len
        for nm in chord:
            pad = tone(note_freq(nm), beat * 4 * 1.02, kind=kind_pad,
                       a=0.08, d=0.3, s=0.7, r=0.4, gain=pad_gain)
            mix(out, pad, at)

    # Lead melody.
    for nm, sb, db in lead:
        at = int(sb * beat * SR)
        v = tone(note_freq(nm), db * beat * 0.95, kind=kind_lead,
                 a=0.01, d=0.08, s=0.6, r=0.12, gain=lead_gain)
        v = reverb(v, delays_ms=(53, 79), decay=0.18)
        mix(out, v, at)

    # Simple kick on beats 1 and 3 of every bar.
    if kick:
        n = int(0.18 * SR)
        for b in range(bars):
            for beat_i in (0, 2):
                at = b * bar_len + int(beat_i * beat * SR)
                k = [0.0] * n
                for i in range(n):
                    t = i / n
                    f = 130 * (1 - t) + 45
                    k[i] = math.sin(2 * math.pi * f * i / SR) * (1 - t) ** 1.4 * 0.5
                mix(out, k, at)

    return soft_clip(out)


def music_gameplay():
    # Upbeat, warm desert-adventure loop in A minor / C major feel, 8 bars.
    bpm = 104
    chords = [
        ["A3", "C4", "E4"],   # Am
        ["F3", "A3", "C4"],   # F
        ["C4", "E4", "G4"],   # C
        ["G3", "B3", "D4"],   # G
        ["A3", "C4", "E4"],   # Am
        ["F3", "A3", "C4"],   # F
        ["C4", "E4", "G4"],   # C
        ["E3", "G#3", "B3"],  # E (lift back to Am)
    ]
    # A pentatonic-ish bouncy lead (beats are 0-indexed across the 8 bars).
    scale = ["A4", "C5", "D5", "E5", "G5"]
    rng = random.Random(11)
    lead = []
    beat = 0.0
    bars = 8
    while beat < bars * 4 - 0.5:
        nm = scale[rng.randrange(len(scale))]
        dur = rng.choice([0.5, 0.5, 1.0])
        lead.append((nm, beat, dur))
        beat += dur
    return build_loop(bpm, bars, chords, lead, kind_pad="triangle",
                      kind_lead="sine", kick=True, lead_gain=0.15, pad_gain=0.08)


def music_hub():
    # Calm, slow merchant-tent loop, sparse arpeggio over warm pads, 8 bars.
    bpm = 72
    chords = [
        ["C4", "E4", "G4"],   # C
        ["A3", "C4", "E4"],   # Am
        ["F3", "A3", "C4"],   # F
        ["G3", "B3", "D4"],   # G
        ["C4", "E4", "G4"],
        ["A3", "C4", "E4"],
        ["D4", "F4", "A4"],   # Dm
        ["G3", "B3", "D4"],   # G
    ]
    # Gentle descending/ascending arpeggio, mostly quarter & half notes.
    pattern = ["E5", "G5", "C6", "G5", "A5", "E5", "D5", "G5"]
    lead = []
    bars = 8
    for b in range(bars):
        for j in range(2):  # two notes per bar, very sparse
            nm = pattern[(b * 2 + j) % len(pattern)]
            lead.append((nm, b * 4 + j * 2, 1.5))
    return build_loop(bpm, bars, chords, lead, kind_pad="triangle",
                      kind_lead="bell", kick=False, lead_gain=0.12, pad_gain=0.10)


# ---------------------------------------------------------------------------

def main():
    os.makedirs(OUT_DIR, exist_ok=True)
    print(f"Generating audio into {OUT_DIR}")
    sfx = {
        "sfx_pickup": sfx_pickup(),
        "sfx_merge": sfx_merge(),
        "sfx_fight_start": sfx_fight_start(),
        "sfx_win": sfx_win(),
        "sfx_loss": sfx_loss(),
    }
    for name, sig in sfx.items():
        write_wav(os.path.join(OUT_DIR, name + ".wav"), sig, normalize_peak=0.9)

    print("Generating music loops (this takes a few seconds)...")
    write_wav(os.path.join(OUT_DIR, "music_gameplay.wav"), music_gameplay(), normalize_peak=0.7)
    write_wav(os.path.join(OUT_DIR, "music_hub.wav"), music_hub(), normalize_peak=0.62)
    print("Done.")


if __name__ == "__main__":
    main()
